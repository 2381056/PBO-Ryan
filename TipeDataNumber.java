public class TipeDataNumber {
    public static void main(String[] args){
        //Bilangan Bulat
        byte tipeDataByte = 10;
        short tipeDataShort = 3276;
        int tipeDataInt = 10;
        long tipeDataLong = 10L;

        //Bilangan Pecahan
        float tipeDataFloat = 4.4f;
        double tipeDataDouble = 5.5;

        //Sistem Bilangan
        int decimalInt = 20;
        int hexDecimal = 0xFF;
        int binDecimal = 0b0101;

        System.out.println(binDecimal);
    }
}
